# Desenvolva um programa que leia quatro notas e que apresente a média final

number1 = int(input('Digite a 1° nota: '))
number2 = int(input('Digite a 2° nota: '))
number3 = int(input('Digite a 3° nota: '))
number4 = int(input('Digite a 4° nota: '))

media = (number1+number2+number3+number4)/4

print(f'A media é {media}\n')